public class ContohTIpeData22 {
    public static void main(String[] args) {
        char golonganDarah = 'O';
        byte jarak = (byte) 200;
        short jumlahPendudukDalamSatuDusun = 1025;
        float suhu = 60.50F;
        double berat = 0.5467812345;
        long saldo = 100000;
        int angkaDesimal = 0x10;
        System.out.println("GolonganDarah\t\t\t\t\t: " + (byte) golonganDarah);
        System.out.println("Jarak\t\t\t\t\t\t: " + jarak);
        System.out.println("JumlahPendudukDusun\t\t\t\t: " + jumlahPendudukDalamSatuDusun);
        System.out.println("Suhu\t\t\t\t\t\t: " + suhu);
        System.out.println("JumlahBerat\t\t\t\t\t: " + (float) berat);
        System.out.println("Saldo\t\t\t\t\t\t: " + saldo);
        System.out.println("AngkaDesimal\t\t\t\t\t: " + angkaDesimal);
    }
}
